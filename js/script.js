//1. Написати функцію з одним аргументом, яка перевіряє чи є аргумент числом. Якщо так, то повертає чи є це цисло парним чи не парним. Якщо аргумент не число - повертає пусту строку.

function checkNum(argument) {
    if (typeof argument == 'number') { // Перевіряє чи передане значення argument є числом
        if (argument % 2 == 0) { // Перевірка остачі від ділення числа argument на 2, якщо остача 0, то argument ділиться націло на 2, тобто є парним
            return `Число ${argument} парне`; // Якщо число є парним то функція повертає повідомлення про це
        }
        if (argument % 2 == 1) { // Перевірка остачі від ділення числа argument на 2, якщо остача 1, то argument не ділиться націло на 2, тобто є непарним
            return `Число ${argument} непарне`; // Якщо число є непарним то функція повертає повідомлення про це
        }
        // Якщо остача від ділення числа argument на 2 не 0 і не 1, то число не є цілим
        return `Число ${argument} не є цілим числом` // Якщо число не є цілим то функція повертає повідомлення про це
    }
    return `${argument} не є числом`; // Якщо передане значення не є числом то функція повертає повідомлення про це
}

