//2. Написати функцію, яка починає перебирати числа від 1 до нескінченності і перевіряє чи є число простим (просте число - те яке ділиться лише на 1 чи само на себе). Якщо просто число знайдене - воно додається до масиву. Після пʼятого знайденого простого числа функція повинна повернути суму всіх пʼяти простих чисел в масиві.

function findSimpleNumbers() {
    let arr = []; // Масив для збереження знайдених чисел
    let sum = 0; // Змінна для збереження суми знайдених чисел
    firstFor: for (let i = 1; i < Infinity; i++) { // Перебирання чисел від 1 до нескінченності
        for (let n = 2; n < i; n++) { // Перевірка ділення числа і націло на попередні числа починаючи з 2
            if (i % n == 0) continue firstFor; // Якщо число i ділиться націло на n, то цикл firstFor переходить до наступної ітерації
        }
        // Якщо число і не ділиться на жодне з попередніх націло, то воно є простим 
        arr.push(i); // Занесення значення простого числа в масив
        if (arr.length == 5) { // Якщо довжина масиву буде 5, тобто знайдених простих чисел буде 5, то функція поверне суму значень елементів масиву
            for (let item of arr) { // Цикл перебирає кожен елемент масиву item = arr[i]
                sum += item; // До змінної sum додається значення кожного елементу масиву
            }
            return sum; // Функція повертає суму значень елементів масиву
        }
    }
}

console.log('Сума перших 5 простих чисел:', findSimpleNumbers()); // Вивід значення функції


