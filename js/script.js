//3. Написати функцію з одним аргументом n, що є числом. Функція повинна повернути суму наступного ряду з n чисел 1 + 11 + 111 + 1111 + ...., де кожний елемент - число з постійно зростаючою кількістю одиниць.

function calcSum(n) {
    let num = 0; // Змінна для збереження доданка суми
    let sum = 0; // Змінна для збереження суми доданків
    for (let i = 1; i <= n; i++) { // Цикл для створення суми n доданків
        num += 10 ** (i - 1); // До доданка додається 10 в степені (i-1). Наприклад, до 3 доданка добавиться 10^(3-1) = 10^2 = 100
        //num += Math.pow(10, i - 1); // Піднесення в степінь також можна записати таким чином
        sum += num; // До змінної sum додається свторений вище доданок
    }
    return sum; // Функція повертає значення суми
}

